# for loop - ის დახმარებით 1 - დან 47 - მდე გამოიტანეთ რიცხვების ჯამი, გამოიყენეთ ( + )ოპერატორი, ხოლო შემდეგ განახორციელეთ იგივე რამ, ( * ) - ოპერატორზე.

# variant 1

total = 0
for i in range(1, 47):
    total += i
print('sum: ', total)



# variant 2

# + operator

for i in range (1 , 47):
    print(i + i)

# * operator

for i in range (1 , 47):
    print(i * i)
